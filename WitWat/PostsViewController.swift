import WitWatKit

class PostsViewController: UITableViewController {

    private var posts: [WWKPost] = [] {
        didSet {
            tableView.reloadData()
        }
    }
    
    func loadThreads() {
        // TODO: Start activity indicator
        WWKServer.defaultServer().queryPostsWithCompletionHandler() { (posts: [WWKPost]) in
            self.posts = posts
            // TODO: Stop activity indicator
        }
    }
    
