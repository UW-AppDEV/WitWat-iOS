import UIKit
import WitWatKit

class CommentsViewController: UITableViewController {

    private var comments: [WWKComment] = [] {
        didSet {
            tableView.reloadData()
        }
    }
    var post: WWKPost? {
        didSet {
            self.navigationItem.title = post?.content
            loadComments()
        }
    }
    
    func loadComments() {
        if let post = post {
            WWKServer.defaultServer().queryCommentsForPost(post) { (comments: [WWKComment]) in
                self.comments = comments
            }
        } else {
            comments = []
        }
    }
    
